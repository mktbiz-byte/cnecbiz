function parseCache(value) {
  if (!value) return null
  if (typeof value === 'object') return value
  try { return JSON.parse(value) } catch { return null }
}

function parseDuration(duration) {
  const match = duration.match(/PT(?:(\d+)H)?(?:(\d+)M)?(?:(\d+)S)?/)
  if (!match) return 0
  return (parseInt(match[1] || 0) * 3600) + (parseInt(match[2] || 0) * 60) + parseInt(match[3] || 0)
}

// 광고/AI 콘텐츠인지 판별
function isAdOrAIContent(title, description) {
  const text = `${title} ${description}`.toLowerCase()

  // 광고 키워드 체크
  for (const keyword of AD_KEYWORDS) {
    if (text.includes(keyword.toLowerCase())) return { blocked: true, reason: 'ad' }
  }

  // AI 키워드 체크
  for (const keyword of AI_KEYWORDS) {
    if (text.includes(keyword.toLowerCase())) return { blocked: true, reason: 'ai' }
  }

  return { blocked: false }
}

// 광고 부스팅 의심 여부 체크
function isSuspectedAdBoosted(viewCount, likeCount, commentCount, subscriberCount) {
  // engagement rate (좋아요/조회수) - 오가닉은 보통 2~10%
  const engagementRate = viewCount > 0 ? (likeCount / viewCount) * 100 : 0

  // 좋아요가 거의 없는데 조회수만 높음 → 광고 부스팅 의심
  if (viewCount >= 500000 && engagementRate < 0.5) return { suspected: true, reason: 'very_low_engagement' }

  // 조회수 100만+ 인데 좋아요가 1000 미만 → 부자연스러운 조회수
  if (viewCount >= 1000000 && likeCount < 1000) return { suspected: true, reason: 'unnatural_views' }

  // 댓글이 0인데 조회수 50만+ → 댓글 비활성화 or 광고
  if (viewCount >= 500000 && commentCount === 0) return { suspected: true, reason: 'no_comments' }

  // 구독자 대비 조회수 비율 체크 (구독자 정보가 있는 경우)
  if (subscriberCount > 0) {
    const viewToSubRatio = viewCount / subscriberCount
    // 구독자 대비 100배 이상 조회수면 바이럴 or 광고 → engagement로 추가 판별
    if (viewToSubRatio > 100 && engagementRate < 1) {
      return { suspected: true, reason: 'abnormal_view_to_sub_ratio' }
    }
  }

  // 구독자가 0이거나 비공개인데 조회수 100만+ → 의심
  if (subscriberCount === 0 && viewCount >= 1000000) {
    return { suspected: true, reason: 'no_subscribers_high_views' }
  }

  return { suspected: false }
}

// 오가닉 품질 점수 계산 (높을수록 좋은 오가닉 콘텐츠)
function calculateOrganicScore(video) {
  let score = 0
  const viewCount = video.view_count
  const likeCount = video.like_count
  const commentCount = video.comment_count

  // 1. Engagement Rate (좋아요/조회수) - 최대 30점
  const engRate = viewCount > 0 ? (likeCount / viewCount) * 100 : 0
  if (engRate >= 5) score += 30
  else if (engRate >= 3) score += 25
  else if (engRate >= 2) score += 20
  else if (engRate >= 1) score += 10

  // 2. 댓글 활성도 - 최대 20점
  const commentRate = viewCount > 0 ? (commentCount / viewCount) * 100 : 0
  if (commentRate >= 0.5) score += 20
  else if (commentRate >= 0.2) score += 15
  else if (commentRate >= 0.05) score += 10
  else if (commentCount > 0) score += 5

  // 3. 조회수 (인기도) - 최대 25점
  if (viewCount >= 5000000) score += 25
  else if (viewCount >= 1000000) score += 20
  else if (viewCount >= 500000) score += 15
  else if (viewCount >= 100000) score += 10

  // 4. 좋아요 절대 수 - 최대 15점
  if (likeCount >= 100000) score += 15
  else if (likeCount >= 50000) score += 12
  else if (likeCount >= 10000) score += 8
  else if (likeCount >= 5000) score += 5

  // 5. 최근 발행 보너스 - 최대 10점
  const daysAgo = (Date.now() - new Date(video.published_at).getTime()) / (1000 * 60 * 60 * 24)
  if (daysAgo <= 7) score += 10
  else if (daysAgo <= 14) score += 7
  else if (daysAgo <= 21) score += 4

  return score
}


exports.handler = async (event) => {
  const headers = {
    'Access-Control-Allow-Origin': '*',
    'Access-Control-Allow-Headers': 'Content-Type',
    'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
    'Content-Type': 'application/json'
  }

  if (event.httpMethod === 'OPTIONS') {
    return { statusCode: 200, headers, body: '' }
  }

  try {
    // 파라미터 파싱
    let country = 'kr'
    let category = ''
    let limit = 10

    if (event.httpMethod === 'POST' && event.body) {
      const body = JSON.parse(event.body)
      country = body.country || 'kr'
      category = body.category || ''
      limit = Math.min(body.limit || 10, 20)
    } else if (event.queryStringParameters) {
      country = event.queryStringParameters.country || 'kr'
      category = event.queryStringParameters.category || ''
      limit = Math.min(parseInt(event.queryStringParameters.limit) || 10, 20)
    }

    // 유효한 국가 체크
    if (!COUNTRY_QUERIES[country]) country = 'kr'

    const cacheKey = `${CACHE_KEY_PREFIX}${country}_${category || 'all'}`

    // 캐시 확인
    const { data: cache } = await supabase
      .from('site_settings')
      .select('value, updated_at')
      .eq('key', cacheKey)
      .maybeSingle()

    if (cache && cache.value) {
      const parsed = parseCache(cache.value)
      if (parsed) {
        const updatedAt = new Date(cache.updated_at)
        const hoursSince = (Date.now() - updatedAt.getTime()) / (1000 * 60 * 60)
        if (hoursSince < CACHE_HOURS) {
          return {
            statusCode: 200,
            headers,
            body: JSON.stringify({
              success: true,
              data: parsed.slice(0, limit),
              total: parsed.length,
              cached: true,
              country,
              category: category || 'all',
            })
          }
        }
      }
    }

    const apiKey = process.env.YOUTUBE_API_KEY || process.env.YOUTUBE_API_KEY_1 || process.env.VITE_YOUTUBE_API_KEY
    if (!apiKey) {
      const staleData = cache && cache.value ? parseCache(cache.value) : null
      if (staleData) {
        return {
          statusCode: 200,
          headers,
          body: JSON.stringify({ success: true, data: staleData.slice(0, limit), total: staleData.length, cached: true, stale: true, country })
        }
      }
      return {
        statusCode: 500,
        headers,
        body: JSON.stringify({ success: false, error: 'YouTube API key not configured' })
      }
    }

    const fetch = (await import('node-fetch')).default
    const countryConfig = COUNTRY_QUERIES[country]

    // 검색 쿼리 구성 - 카테고리가 있으면 카테고리 키워드 추가
    let queries = [...countryConfig.queries]
    if (category && CATEGORY_KEYWORDS[category]) {
      const catKeyword = CATEGORY_KEYWORDS[category][country] || CATEGORY_KEYWORDS[category]['us']
      // 카테고리 키워드를 앞 4개 쿼리에 추가
      queries = queries.slice(0, 4).map(q => `${catKeyword} ${q}`)
    }

    // 검색어 4개 사용 (API 쿼터 고려)
    const queriesToUse = queries.slice(0, 4)
    const allVideoIds = new Set()
    const videoIdList = []
    const publishedAfter = new Date(Date.now() - 30 * 24 * 60 * 60 * 1000).toISOString()

    for (const query of queriesToUse) {
      try {
        const searchUrl = `https://www.googleapis.com/youtube/v3/search?` +
          `part=snippet&type=video&videoDuration=short&maxResults=25` +
          `&q=${encodeURIComponent(query)}` +
          `&regionCode=${countryConfig.regionCode}` +
          `&relevanceLanguage=${countryConfig.relevanceLanguage}` +
          `&order=viewCount` +
          `&publishedAfter=${publishedAfter}` +
          `&key=${apiKey}`

        const searchRes = await fetch(searchUrl)
        const searchData = await searchRes.json()

        if (searchData.items) {
          for (const item of searchData.items) {
            const vid = item.id.videoId
            const channelId = item.snippet.channelId

            if (vid && !allVideoIds.has(vid)) {
              // 1차 필터: 제목에서 광고/AI 판별
              const titleCheck = isAdOrAIContent(item.snippet.title, item.snippet.description || '')
              if (titleCheck.blocked) continue

              allVideoIds.add(vid)
              videoIdList.push({ videoId: vid, channelId })
            }
          }
        }
      } catch (err) {
        console.error(`[fetch-trending-beauty-shorts] Search error for "${query}":`, err.message)
      }
    }

    if (videoIdList.length === 0) {
      const staleData = cache && cache.value ? parseCache(cache.value) : null
      return {
        statusCode: 200,
        headers,
        body: JSON.stringify({ success: true, data: staleData || [], total: 0, cached: !!staleData, country })
      }
    }

    // 비디오 상세 정보 가져오기 (배치 처리, 50개씩)
    const allVideoDetails = []
    for (let i = 0; i < videoIdList.length; i += 50) {
      const batch = videoIdList.slice(i, i + 50)
      const videosRes = await fetch(
        `https://www.googleapis.com/youtube/v3/videos?` +
        `part=snippet,statistics,contentDetails` +
        `&id=${batch.map(v => v.videoId).join(',')}` +
        `&key=${apiKey}`
      )
      const videosData = await videosRes.json()
      if (videosData.items) {
        allVideoDetails.push(...videosData.items)
      }
    }

    // 채널 정보 가져오기 (구독자 수 확인용)
    const uniqueChannelIds = [...new Set(allVideoDetails.map(v => v.snippet.channelId))]
    const channelSubMap = new Map()

    for (let i = 0; i < uniqueChannelIds.length; i += 50) {
      const batch = uniqueChannelIds.slice(i, i + 50)
      try {
        const channelsRes = await fetch(
          `https://www.googleapis.com/youtube/v3/channels?` +
          `part=statistics` +
          `&id=${batch.join(',')}` +
          `&key=${apiKey}`
        )
        const channelsData = await channelsRes.json()
        if (channelsData.items) {
          for (const ch of channelsData.items) {
            channelSubMap.set(ch.id, {
              subscriberCount: parseInt(ch.statistics.subscriberCount) || 0,
              hiddenSubscriberCount: ch.statistics.hiddenSubscriberCount || false,
            })
          }
        }
      } catch (err) {
        console.error('[fetch-trending-beauty-shorts] Channel fetch error:', err.message)
      }
    }

    // 필터링 + 매핑
    const filteredShorts = []

    for (const video of allVideoDetails) {
      // Shorts 필터 (60초 이하)
      const totalSeconds = parseDuration(video.contentDetails.duration)
      if (totalSeconds <= 0 || totalSeconds > 60) continue

      const viewCount = parseInt(video.statistics.viewCount) || 0
      const likeCount = parseInt(video.statistics.likeCount) || 0
      const commentCount = parseInt(video.statistics.commentCount) || 0
      const channelId = video.snippet.channelId
      const channelInfo = channelSubMap.get(channelId) || { subscriberCount: 0 }

      // 조건 1: 10만뷰 이상
      if (viewCount < MIN_VIEW_COUNT) continue

      // 조건 2: 광고/AI 콘텐츠 제외 (상세 설명으로 2차 필터)
      const contentCheck = isAdOrAIContent(
        video.snippet.title,
        video.snippet.description || ''
      )
      if (contentCheck.blocked) continue

      // 조건 5: 광고 부스팅 의심 제외
      const boostCheck = isSuspectedAdBoosted(
        viewCount,
        likeCount,
        commentCount,
        channelInfo.subscriberCount
      )
      if (boostCheck.suspected) continue

      // engagement rate 최소 기준 (1% 이상만)
      const engRate = viewCount > 0 ? (likeCount / viewCount) * 100 : 0
      if (engRate < 1) continue

      filteredShorts.push({
        video_id: video.id,
        title: video.snippet.title,
        description: (video.snippet.description || '').slice(0, 200),
        thumbnail: video.snippet.thumbnails?.maxres?.url
          || video.snippet.thumbnails?.high?.url
          || video.snippet.thumbnails?.medium?.url
          || video.snippet.thumbnails?.default?.url,
        channel_id: channelId,
        channel_title: video.snippet.channelTitle,
        view_count: viewCount,
        like_count: likeCount,
        comment_count: commentCount,
        subscriber_count: channelInfo.subscriberCount,
        engagement_rate: Math.round(engRate * 100) / 100,
        published_at: video.snippet.publishedAt,
        duration_seconds: totalSeconds,
        url: `https://youtube.com/shorts/${video.id}`,
      })
    }

    // 오가닉 점수로 정렬
    filteredShorts.sort((a, b) => {
      const scoreA = calculateOrganicScore(a)
      const scoreB = calculateOrganicScore(b)
      return scoreB - scoreA
    })

    // 채널당 최대 1개만 (조건 3)
    const selectedChannels = new Set()
    const shorts = []
    for (const short of filteredShorts) {
      if (selectedChannels.has(short.channel_id)) continue
      selectedChannels.add(short.channel_id)

      // 오가닉 점수 추가
      short.organic_score = calculateOrganicScore(short)

      shorts.push(short)
      if (shorts.length >= 20) break // 캐시에는 최대 20개 저장
    }

    // 캐시 저장
    try {
      await supabase
        .from('site_settings')
        .upsert({
          key: cacheKey,
          value: JSON.stringify(shorts),
          updated_at: new Date().toISOString()
        }, { onConflict: 'key' })
    } catch (cacheErr) {
      console.error('[fetch-trending-beauty-shorts] Cache save error:', cacheErr.message)
    }

    return {
      statusCode: 200,
      headers,
      body: JSON.stringify({
        success: true,
        data: shorts.slice(0, limit),
        total: shorts.length,
        cached: false,
        country,
        category: category || 'all',
        filters: {
          minViews: MIN_VIEW_COUNT,
          adFiltered: true,
          aiFiltered: true,
          boostFiltered: true,
          onePerChannel: true,
        }
      })
    }
  } catch (error) {
    console.error('[fetch-trending-beauty-shorts] Error:', error)
    return {
      statusCode: 500,
      headers,
      body: JSON.stringify({ success: false, error: error.message })
    }
  }
}
